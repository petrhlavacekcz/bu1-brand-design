/**
 * Strips custom BU1 HTML from generated .md files so they contain clean Markdown.
 * Works alongside starlight-page-actions which only strips Starlight components.
 *
 * Runs at build time (astro:build:done) — cleans .md files in dist/ so the
 * deployed Copy Markdown and View Markdown serve pure Markdown without HTML.
 *
 * Dev mode limitation: vite-plugin-static-copy serves .md files via its own
 * middleware which can't be overridden. Dev .md output contains raw HTML.
 */
import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

/** Clean HTML from markdown content, preserving text and markdown syntax. */
function cleanHtml(content) {
  let r = content;

  // Preserve code blocks from being mangled
  const codeBlocks = [];
  r = r.replace(/(```[\s\S]*?```)/g, (_, block) => {
    codeBlocks.push(block);
    return `__CODE_BLOCK_${codeBlocks.length - 1}__`;
  });

  // Convert semantic HTML to markdown equivalents
  r = r.replace(/<strong>(.*?)<\/strong>/gs, '**$1**');
  r = r.replace(/<em>(.*?)<\/em>/gs, '*$1*');
  r = r.replace(/<code>(.*?)<\/code>/gs, '`$1`');
  r = r.replace(/<a\s+href="([^"]*)"[^>]*>(.*?)<\/a>/gs, '[$2]($1)');

  // Convert <li> to markdown list items before stripping tags
  r = r.replace(/<li[^>]*>([\s\S]*?)<\/li>/gs, (_, inner) => {
    const text = inner.replace(/<[^>]+>/g, '').trim();
    return `- ${text}\n`;
  });

  // Remove <ul> and <ol> wrappers (list items already converted)
  r = r.replace(/<\/?(?:ul|ol)[^>]*>/g, '');

  // Convert <table> blocks to markdown tables
  r = r.replace(/<table[\s\S]*?<\/table>/gs, (table) => {
    const rows = [];
    const headerCells = [];
    table.replace(/<th[^>]*>([\s\S]*?)<\/th>/g, (_, cell) => {
      headerCells.push(cell.replace(/<[^>]+>/g, '').trim());
    });
    if (headerCells.length) {
      rows.push('| ' + headerCells.join(' | ') + ' |');
      rows.push('|' + headerCells.map(() => '---').join('|') + '|');
    }
    table.replace(/<tr[\s\S]*?<\/tr>/gs, (row) => {
      if (/<th/.test(row)) return;
      const cells = [];
      row.replace(/<td[^>]*>([\s\S]*?)<\/td>/gs, (_, cell) => {
        cells.push(cell.replace(/<[^>]+>/g, '').trim());
      });
      if (cells.length) rows.push('| ' + cells.join(' | ') + ' |');
    });
    return rows.length ? '\n' + rows.join('\n') + '\n' : '';
  });

  // Strip all remaining HTML tags, keeping text content
  r = r.replace(/<[^>]+>/g, '');

  // Decode common HTML entities
  r = r.replace(/&amp;/g, '&');
  r = r.replace(/&lt;/g, '<');
  r = r.replace(/&gt;/g, '>');
  r = r.replace(/&nbsp;/g, ' ');

  // Restore code blocks
  r = r.replace(/__CODE_BLOCK_(\d+)__/g, (_, i) => codeBlocks[Number(i)]);

  // Remove indentation from lines that were inside HTML blocks
  r = r.split('\n').map((line) => {
    // Don't strip indent from markdown list items, code blocks, or blockquotes
    if (/^(\s*[-*+]\s|>\s|```|\s{4,}\S)/.test(line)) return line;
    return line.replace(/^\s{2,}/, '');
  }).join('\n');

  // Normalize excessive blank lines
  r = r.replace(/\n{3,}/g, '\n\n');

  // Trim trailing whitespace on each line
  r = r.split('\n').map((l) => l.trimEnd()).join('\n');

  return r.trim() + '\n';
}

/** Recursively find all .md files in a directory */
async function findMdFiles(dir) {
  const files = [];
  try {
    for (const entry of await readdir(dir, { withFileTypes: true })) {
      const full = join(dir, entry.name);
      if (entry.isDirectory()) files.push(...(await findMdFiles(full)));
      else if (entry.name.endsWith('.md')) files.push(full);
    }
  } catch { /* directory doesn't exist */ }
  return files;
}

/** Astro integration — cleans .md files generated by starlight-page-actions. */
export default function cleanMarkdownIntegration() {
  return {
    name: 'bu1-clean-markdown',
    hooks: {
      'astro:build:done': async ({ dir }) => {
        const distPath = fileURLToPath(dir);
        const mdFiles = await findMdFiles(distPath);
        let cleaned = 0;
        for (const file of mdFiles) {
          const content = await readFile(file, 'utf-8');
          const result = cleanHtml(content);
          if (result !== content) {
            await writeFile(file, result, 'utf-8');
            cleaned++;
          }
        }
        if (mdFiles.length) {
          console.log(`[bu1-clean-markdown] Cleaned ${cleaned}/${mdFiles.length} .md files`);
        }
      },
    },
  };
}
